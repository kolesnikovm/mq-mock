package config;

public class SearchConfig {

    private int port;
    private String path;

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String host) {
        this.path = host;
    }
}
